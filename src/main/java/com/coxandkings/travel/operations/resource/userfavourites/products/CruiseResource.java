package com.coxandkings.travel.operations.resource.userfavourites.products;

public class CruiseResource {

    private String destination;
    private String country;
    private String city;
    private String cruiseBrandName;
    private String shipName;
    private String cruisePackageName;
    private String itineraryName;
    private String supplierName;
    private String supplierReferenceNo;

    public String getDestination() {
        return destination;
    }

    public void setDestination(String destination) {
        this.destination = destination;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCruiseBrandName() {
        return cruiseBrandName;
    }

    public void setCruiseBrandName(String cruiseBrandName) {
        this.cruiseBrandName = cruiseBrandName;
    }

    public String getShipName() {
        return shipName;
    }

    public void setShipName(String shipName) {
        this.shipName = shipName;
    }

    public String getCruisePackageName() {
        return cruisePackageName;
    }

    public void setCruisePackageName(String cruisePackageName) {
        this.cruisePackageName = cruisePackageName;
    }

    public String getItineraryName() {
        return itineraryName;
    }

    public void setItineraryName(String itineraryName) {
        this.itineraryName = itineraryName;
    }

    public String getSupplierName() {
        return supplierName;
    }

    public void setSupplierName(String supplierName) {
        this.supplierName = supplierName;
    }

    public String getSupplierReferenceNo() {
        return supplierReferenceNo;
    }

    public void setSupplierReferenceNo(String supplierReferenceNo) {
        this.supplierReferenceNo = supplierReferenceNo;
    }
}
