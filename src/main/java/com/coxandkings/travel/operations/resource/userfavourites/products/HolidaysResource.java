package com.coxandkings.travel.operations.resource.userfavourites.products;

public class HolidaysResource {

    private String destination;
    private String country;
    private String city;
    private String numOfNights;
    private String productName;
    private String productFlavorName;
    private String packageType;
    private String brand;
    private String companyPackageCategory;
    private String supplierName;
    private String supplierReferenceNo;


    public String getDestination() {
        return destination;
    }

    public void setDestination(String destination) {
        this.destination = destination;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getNumOfNights() {
        return numOfNights;
    }

    public void setNumOfNights(String numOfNights) {
        this.numOfNights = numOfNights;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductFlavorName() {
        return productFlavorName;
    }

    public void setProductFlavorName(String productFlavorName) {
        this.productFlavorName = productFlavorName;
    }

    public String getPackageType() {
        return packageType;
    }

    public void setPackageType(String packageType) {
        this.packageType = packageType;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getCompanyPackageCategory() {
        return companyPackageCategory;
    }

    public void setCompanyPackageCategory(String companyPackageCategory) {
        this.companyPackageCategory = companyPackageCategory;
    }

    public String getSupplierName() {
        return supplierName;
    }

    public void setSupplierName(String supplierName) {
        this.supplierName = supplierName;
    }

    public String getSupplierReferenceNo() {
        return supplierReferenceNo;
    }

    public void setSupplierReferenceNo(String supplierReferenceNo) {
        this.supplierReferenceNo = supplierReferenceNo;
    }
}


