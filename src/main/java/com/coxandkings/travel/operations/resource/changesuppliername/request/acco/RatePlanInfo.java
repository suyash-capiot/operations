
package com.coxandkings.travel.operations.resource.changesuppliername.request.acco;


public class RatePlanInfo {

    private String ratePlanName;
    private String ratePlanRef;
    private String ratePlanCode;
    private String bookingRef;

    public String getRatePlanName() {
        return ratePlanName;
    }

    public void setRatePlanName(String ratePlanName) {
        this.ratePlanName = ratePlanName;
    }

    public String getRatePlanRef() {
        return ratePlanRef;
    }

    public void setRatePlanRef(String ratePlanRef) {
        this.ratePlanRef = ratePlanRef;
    }

    public String getRatePlanCode() {
        return ratePlanCode;
    }

    public void setRatePlanCode(String ratePlanCode) {
        this.ratePlanCode = ratePlanCode;
    }

    public String getBookingRef() {
        return bookingRef;
    }

    public void setBookingRef(String bookingRef) {
        this.bookingRef = bookingRef;
    }

}
